        </h3>
          Si l’ambulatoire permet d’absorber une grande partie de l’activité hospitalière, il n’en demeure pas moins que des progrès significatifs en termes de revalidation précoce peuvent être apportés sur le reliquat en hospitalisation complète. 
          ERAS (enhanced rehabilitation after surgery) ou RRAC dans la littérature française (réhabilitation rapide après chirurgie programmée) est une approche de prise en charge globale du patient en périopératoire visant au rétablissement rapide des capacités physiques et psychiques antérieures et donc à réduire significativement la mortalité et la morbidité. Elle a aussi pour conséquence in fine de voir réduire les durées d’hospitalisations. Les expérimentations documentées mettent en avant une réduction de 30% des durées de séjours et une réduction des complications de 50%.

          Nos consultants ont obtenu la certification ERAS en 2015 (ERAS Congress Washington DC) et proposent de vous accompagner pour la mise en place de la Récupération Rapide Après Chirurgie dans vos établissements. 
          Démarche extrêmement porteuse et avancée, elle correspond à une organisation spécifique des soins selon la notion de « chemin clinique » centrée autour du patient qui joue un rôle actif, accompagné d’une prise en charge multidisciplinaire. Nos consultants co-élaboreront avec vos équipes ce parcours en retenant à chaque étape l’angle de l’optimisation de la prise en charge et des concepts de sécurité et qualité de la prise en charge. Pour cela, une protocolisation adaptée à la typologie de prise en charge, de patient et de contexte local est nécessaire. 
